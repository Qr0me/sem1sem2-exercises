num1 = int(input("Number1: "))
num2 = int(input("Number2: "))
operator = input("What Operation: ")

if operator=="+":
    print(num1+num2)
elif operator=="-":
    print(num1-num2)
elif operator=="*":
    print(num1*num2)
elif operator=="/":
    print(num1/num2)
else:
    print("Not a mathematical operator")

